package com.tourbuddy.base.entities;

/**
 *
 * @author Sanduni
 */
public class Profile {
    
    private String username;
    private String areYou;
    private String relationship;
    private String religious;
    private String aboutMe;
    private String concern;
    private String travelFor;
    private String visitedBefore;
    private String challenging;
    private String reason;
    private String often;

    public Profile() {
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getAreYou() {
        return areYou;
    }

    public void setAreYou(String areYou) {
        this.areYou = areYou;
    }

    public String getRelationship() {
        return relationship;
    }

    public void setRelationship(String relationship) {
        this.relationship = relationship;
    }

    public String getReligious() {
        return religious;
    }

    public void setReligious(String religious) {
        this.religious = religious;
    }

    public String getAboutMe() {
        return aboutMe;
    }

    public void setAboutMe(String aboutMe) {
        this.aboutMe = aboutMe;
    }

    public String getConcern() {
        return concern;
    }

    public void setConcern(String concern) {
        this.concern = concern;
    }

    public String getTravelFor() {
        return travelFor;
    }

    public void setTravelFor(String travelFor) {
        this.travelFor = travelFor;
    }

    public String getVisitedBefore() {
        return visitedBefore;
    }

    public void setVisitedBefore(String visitedBefore) {
        this.visitedBefore = visitedBefore;
    }

    public String getChallenging() {
        return challenging;
    }

    public void setChallenging(String challenging) {
        this.challenging = challenging;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getOften() {
        return often;
    }

    public void setOften(String often) {
        this.often = often;
    }    
}
